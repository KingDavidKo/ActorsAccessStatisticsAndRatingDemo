CREATE TABLE ACTOR_RATE_STATISTICS (
	id CHARACTER VARYING(16) NOT NULL,
    actorID CHARACTER VARYING(16) NOT NULL,
    actorFirstName CHARACTER VARYING(256) NOT NULL,
    actorLastName CHARACTER VARYING(256) NOT NULL,
    actorEmail CHARACTER VARYING(256) NOT NULL,
    type VARCHAR(255),
    project VARCHAR(255),
    role VARCHAR(255),
    activityDate DATE NOT NULL,
    creationDate DATE NOT NULL,
    days NUMERIC(9),
    agentName CHARACTER VARYING(256) NOT NULL,
    agentEmail CHARACTER VARYING(256) NOT NULL,
    CONSTRAINT PK_ACTORRATE PRIMARY KEY (id)
);

CREATE SEQUENCE seq_actor MINVALUE 1 START WITH 1 INCREMENT BY 1 CACHE 10;

